//Add and subtract for snacks
let addSnack1 = document.getElementById("add-snack-1");
let removeSnack1 = document.getElementById("remove-snack-1");

let numSnack1 = document.getElementById("num-snack-1");
let a = 0;

addSnack1.addEventListener("click", function(){
    a++;
    numSnack1.innerHTML = a;
});

removeSnack1.addEventListener("click", function(){
    if (a > 0) {
        a--;
    }
    numSnack1.innerHTML = a;
});


